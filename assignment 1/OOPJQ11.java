class OOPJQ11{
	public static void main(String [] args){
	for(int i = 70; i >=65; i--){
			for(int j = 65; j <=i; j++){
			
				System.out.print((char)j+" ");
			}
			System.out.println();
		}
	for(int k= 65; k<=70; k++){
			for(int l = 65; l <=k; l++){
			
				System.out.print((char)l+" ");
			}
			System.out.println();
	}
	
	}
}