import java.util.*;
class Switchh{
	public static void main(String []args){
		Scanner sc = new Scanner(System.in);
		char vow = sc.next().charAt(0);
		switch(vow){
			case 'a':
			            System.out.println("It is a vowel");
			            break;
			case 'e':
			            System.out.println("It is a vowel");
			            break;
			case 'i':
			            System.out.println("It is a vowel");
			            break;
			case 'o':
			            System.out.println("It is a vowel");
			            break;
			case 'u':
			            System.out.println("It is a vowel");
			            break;
			default:
			            System.out.println("It is not a vowel");
			            break;
			
		}
		
		
	}
}